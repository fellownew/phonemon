package jdy;

public class Monster {
	private String cd;
	private String name;
	private String hp;
	private String nAtk;
	private String nDef;
	private String sAtk;
	private String sDef;
	private String spd;

	public String getCd() {
		return cd;
	}

	public void setCd(String cd) {
		this.cd = cd;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getHp() {
		return hp;
	}

	public void setHp(String hp) {
		this.hp = hp;
	}

	public String getnAtk() {
		return nAtk;
	}

	public void setnAtk(String nAtk) {
		this.nAtk = nAtk;
	}

	public String getnDef() {
		return nDef;
	}

	public void setnDef(String nDef) {
		this.nDef = nDef;
	}

	public String getsAtk() {
		return sAtk;
	}

	public void setsAtk(String sAtk) {
		this.sAtk = sAtk;
	}

	public String getsDef() {
		return sDef;
	}

	public void setsDef(String sDef) {
		this.sDef = sDef;
	}

	public String getSpd() {
		return spd;
	}

	public void setSpd(String spd) {
		this.spd = spd;
	}

}
